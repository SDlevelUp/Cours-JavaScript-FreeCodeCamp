// Explore Differences Between the var and let Keywords

// var catName = "Oliver";
// var catSound = "Meow!";

// Solution

let catName = "Oliver";
let catSound = "Meow!";

/* "let" a une portée de bloc et permet une réaffectation de la variable.
"var" a une portée de fonction ou globale et permet également une réaffectation de la variable. */