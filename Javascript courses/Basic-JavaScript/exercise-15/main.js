// Increment a Number with JavaScript

let myVar = 87;

/*Change the code to use the ++ operator on myVar.*/
//Solution

myVar ++;




/* L'incrémentation est une opération qui consiste à ajouter 1 à 
 une variable numérique existante. En JavaScript,on peut réaliser 
 cette opération à l'aide de l'opérateur "++" qui s'appelle 
 l'opérateur d'incrémentation.

 Précision :
 L'incrémentation préfixe : l'opérateur "++" est placé avant la variable. */